#include <iostream>
using namespace std;
int main() {
    typedef long long ll; // possible to give a short name to a data type
    ll a = 1000000000; // use the short name to declare a variable
    ll b = 2000000000; // use the short name to declare another variable
    cout << a*b << endl; // output the product of the two variables
    return 0;
}
